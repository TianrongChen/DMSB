def timestep_embedding(timesteps, dim, max_period=10000):
    """
    Create sinusoidal timestep embeddings.

    :param timesteps: a 1-D Tensor of N indices, one per batch element.
                      These may be fractional.
    :param dim: the dimension of the output.
    :param max_period: controls the minimum frequency of the embeddings.
    :return: an [N x dim] Tensor of positional embeddings.
    """
    half = dim // 2
    freqs = torch.exp(
        -math.log(max_period) * torch.arange(start=0, end=half, dtype=torch.float32) / half
    ).to(device=timesteps.device)
    args = timesteps[:, None].float() * freqs[None]
    embedding = torch.cat([torch.cos(args), torch.sin(args)], dim=-1)
    if dim % 2:
        embedding = torch.cat([embedding, torch.zeros_like(embedding[:, :1])], dim=-1)
    return embedding

class SiLU(nn.Module):
    def forward(self, x):
        return x * torch.sigmoid(x)

class Cos(nn.Module):
    def forward(self, x):
        return torch.cos(x)

class GroupNorm32(nn.GroupNorm):
    def forward(self, x):
        return super().forward(x.float()).type(x.dtype)

class FCs(torch.nn.Module):
    def __init__(self, dim_in,dim_hid,dim_out):
        super(FCs,self).__init__()
        self.model = nn.Sequential(
            nn.Linear(dim_in, dim_hid),
            nn.ReLU(),
            nn.Linear(dim_hid,dim_out),
            nn.Identity()
            )
    def forward(self,x):
        return self.model(x)

class ResNet_FC(nn.Module):
    def __init__(self, data_dim, hidden_dim, num_res_blocks,device):
        super().__init__()
        self.hidden_dim = hidden_dim
        self.map=nn.Linear(data_dim, hidden_dim)
        self.map_out = nn.Linear(hidden_dim, hidden_dim)
        self.res_blocks = nn.ModuleList(
            [self.build_res_block() for _ in range(num_res_blocks)])

        self.const=torch.sqrt(torch.tensor([2]).to(device))
    def build_linear(self, in_features, out_features):
        linear = nn.Linear(in_features, out_features)
        return linear

    def build_res_block(self):
        hid = self.hidden_dim
        layers = []
        widths =[hid]*4
        for i in range(len(widths) - 1):
            layers.append(torch.jit.script(SiLU()))
            layers.append(self.build_linear(widths[i], widths[i + 1]))
        return nn.Sequential(*layers)

    def forward(self, x, tembed):
        h=self.map(x)
        for res_block in self.res_blocks:
            h = (h + self.map_out(res_block(h)+tembed)) / self.const
        return h

